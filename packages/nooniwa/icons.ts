export type IconName = keyof typeof Icons;

export const FilledIcons = new Set<IconName>([
  "github",
  "x.com",
  "instagram",
  "youtube",
  "rss",
]);

export function getIconAttrs(name: IconName): Record<string, string> {
  return FilledIcons.has(name)
    ? { fill: "currentColor" }
    : {
        fill: "none",
        stroke: "currentColor",
        "stroke-width": "2",
        "stroke-linecap": "round",
        "stroke-linejoin": "round",
      };
}

export function renderIconHtml(name: IconName, size: number): string {
  const attrsStr = Object.entries(getIconAttrs(name))
    .map(([k, v]) => `${k}="${v}"`)
    .join(" ");
  return `<svg xmlns="http://www.w3.org/2000/svg" width="${size}" height="${size}" viewBox="0 0 24 24" ${attrsStr}>${Icons[name]}</svg>`;
}
